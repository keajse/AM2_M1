package com.example.am2_m1_adaptadores_listview;

public class ModeloDos {

    private String etnombres;
    private String ettelefono;


    public ModeloDos(String etnombres, String ettelefono){
        this.etnombres = etnombres;
        this.ettelefono = ettelefono;
    }


    public String getEtnombres() {
        return etnombres;
    }

    public void setEtnombres(String etnombres) {
        this.etnombres = etnombres;
    }

    public String getEttelefono() {
        return ettelefono;
    }

    public void setEttelefono(String ettelefono) {
        this.ettelefono = ettelefono;
    }



}
